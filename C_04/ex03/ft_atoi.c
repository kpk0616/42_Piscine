int	ft_atoi(char *str)
{
	int	sign;
	int	res;

	res = 0;
	sign = 1;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	while (*str)
	{
		while (*str == '-' || *str == '+')
		{
			if (*str == '-')
				sign *= -1;
			str++;
		}
		while (*str >= '0' && *str <= '9')
		{
			res *= 10;
			res += *str - '0';
			str++;
		}
		return (res * sign);
	}
	return (0);
}
